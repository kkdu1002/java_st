package jang3;

public class Ex_04_01 
{
	public static void main(String[] args) 
	{
		int num1 = 5,num2 = 3;
		
		System.out.println(num1 + " " + "+" + " " + num2 + " " + "=" + " " + (num1 + num2));
		System.out.printf("%d - %d = %d\n",num1,num2,num1 - num2);
		System.out.println(num1 + " " + "*" + " " + num2 + " " + "=" + " " + num1 * num2);
		System.out.printf("%d / %d = %d\n",num1,num2,num1 / num2);
		System.out.printf("%d %% %d = %d\n",num1,num2,num1 % num2);
	}
}
