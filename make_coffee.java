package jang12;

import java.util.Scanner;

public class make_coffee 
{
	public static int coffee_machine(int coffee) 
	{
		System.out.printf("\n# 1. 뜨거운 물을 준비한다\n");
		System.out.printf("# 2. 종이컵을 준비한다\n");
		switch(coffee) 
		{
		case 1:
			System.out.printf("# 3. 보통커피를 탄다\n");
			break;
		case 2:
			System.out.printf("# 3. 설탕커피를 탄다\n");
			break;
		case 3:
			System.out.printf("# 3. 블랙커피를 탄다\n");
			break;
		default:
			System.out.printf("# 3. 아무거나 탄다\n");
			break;
		}
		System.out.printf("# 4. 물을 붓는다\n");
		System.out.printf("# 5. 스푼으로 저어서 녹인다\n\n");
		return coffee;
	}
	public static void main(String[] args) 
	{
		Scanner s = new Scanner(System.in);
		int coffee , ret;
		
		System.out.printf("A님, 어떤 커피 드릴까요 ? (1 : 보통, 2: 설탕 , 3 : 블랙)");
		coffee = s.nextInt();
		ret = coffee_machine(coffee);
		System.out.printf("A님~ %d번 커피 여기있습니다.\n",ret);
		
		System.out.printf("B님, 어떤 커피 드릴까요 ? (1 : 보통, 2: 설탕 , 3 : 블랙)");
		coffee = s.nextInt();
		ret = coffee_machine(coffee);
		System.out.printf("B님~ %d번 커피 여기있습니다.\n",ret);
		
		System.out.printf("C님, 어떤 커피 드릴까요 ? (1 : 보통, 2: 설탕 , 3 : 블랙)");
		coffee = s.nextInt();
		ret = coffee_machine(coffee);
		System.out.printf("C님~ %d번 커피 여기있습니다.\n",ret);
	}
}